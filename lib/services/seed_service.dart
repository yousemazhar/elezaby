  Future<void> _seedSubcategories() async {
    final batch = _db.batch();
    for (final s in kSubcategories) {
      final ref = _db.collection('subcategories').doc(s.id);
      batch.set(ref, {
        'categoryId': s.categoryId,
        'name': s.name,
        'emoji': s.emoji,
        'imageUrl': '',
        'sortOrder': s.sortOrder,
      });
    }
    await batch.commit();
